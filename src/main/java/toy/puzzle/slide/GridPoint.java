package toy.puzzle.slide;

/**
 * 网格中的点
 *
 * Created by cc on 16/5/9.
 */
public class GridPoint {

    private int x;

    private int y;

    public GridPoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
